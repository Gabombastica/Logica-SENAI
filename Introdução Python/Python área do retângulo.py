# Python área do retângulo

comprimento_retangulo = float(input("Digite um número para representar o comprimento de um retângulo: "))
largura_retangulo = float(input("Digite outro número para representar a largura do retângulo: "))
multiplicacao_retangulo = comprimento_retangulo * largura_retangulo
area_quadrado = multiplicacao_retangulo
print(f"O resultado da área do retângulo é de {area_quadrado} cm².")