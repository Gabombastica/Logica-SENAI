# Hora de praticar
# Atividade 1

primeiro_numero = float(input("Digite um número: "))
segundo_numero = float(input("Digite outro número: "))

#Soma
resultado_soma = primeiro_numero + segundo_numero
print(f"O resultado da soma é {resultado_soma}")

#Subtração
resultado_subtracao = primeiro_numero - segundo_numero
print(f"O resultado da subtração é {resultado_subtracao}")

#Multiplicação
resultado_multiplicacao = primeiro_numero * segundo_numero
print(f"O resultado da multiplicação é {resultado_multiplicacao}")

#Divisão
resultado_divisao = primeiro_numero / segundo_numero
print(f"O resultado da divisão é {resultado_divisao}")


