# Atividade Avaliativa Salário

salario_atual = float(input("Olá, adicione seu salário atual para a realização do cálculo: "))
salario_divisao = salario_atual / 10
resultado = salario_atual + salario_divisao
print(f" Seu novo salário é igual a R${resultado} reais e teve um aumento de R${salario_divisao} reais, ou seja, uma aumento de 10%.")